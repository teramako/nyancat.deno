
export const NyanCat = {
    start(fps = 6) {
        this.stopped = false;
        print(NEW_SCREEN);
        this.render(fps);
    },
    stopped: true,
    stop() {
        this.stopped = true;
        print(EXIT_SCREEN);
        Deno.exit(0);
    },
    *[Symbol.iterator]() {
        const length = FRAMES.length;
        while (true) {
            for (let i = 0; i < length; ++i) {
                yield FRAMES[i];
            }
        }
    },
    async render(fps: number) {
        for (const frame of this) {
            if (this.stopped) {
                break;
            }
            print(frame);
            await sleep(1/fps);
            print(CLEAR_SCREEN);
        }
    }
};

function print(msg: Uint8Array) {
    Deno.stdout.write(msg);
}
function sleep(sec: number) {
    return new Promise(r => setTimeout(r, sec * 1000));
}

